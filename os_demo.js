const os = require("os");

// Get Platform
console.log(os.platform());

// Get CPU Arch
console.log(os.arch());

// Get CPU Core Info
console.log(os.cpus());

// Get Free Memory
console.log(os.freemem());

// Get Total memory
console.log(os.totalmem());

// Get Home Directory
console.log(os.homedir());

// Get Uptime
console.log(os.uptime());
